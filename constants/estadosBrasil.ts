/** Converte UF antiga ou valor salvo para o nome usado no seletor. */
export function normalizeEstadoParaListaExibicao(valor: string | null | undefined): string {
  if (!valor?.trim()) return '';
  const t = valor.trim();
  if ((ESTADOS_BRASIL as readonly string[]).includes(t)) return t;
  if (t.length === 2) {
    const nome = UF_PARA_NOME[t.toUpperCase()];
    if (nome) return nome;
  }
  return t;
}
